const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');
const { downloadRepoZip } = require('../utils/fetch');
const { extractSkillFolder } = require('../utils/extract');
const { printSuccessMessage } = require('../utils/print');
const { injectAgentInstructions } = require('../utils/agentFiles');

/**
 * Add a skill to the current project
 * @param {string} skillName - Name of the skill to add
 */
async function addSkill(skillName) {
  try {
    // Check if package.json exists
    const packageJsonPath = path.join(process.cwd(), 'package.json');
    if (!fs.existsSync(packageJsonPath)) {
      console.error('Error: package.json not found in current directory');
      console.error('Please run this command in a project directory with package.json');
      process.exit(1);
    }
    
    // Check if skill already exists
    const skillsDir = path.join(process.cwd(), 'skills');
    const skillPath = path.join(skillsDir, skillName);
    
    if (fs.existsSync(skillPath)) {
      console.log(`Skill "${skillName}" already exists at ${skillPath}`);
      console.log('Remove it first if you want to reinstall it.');
      process.exit(0);
    }

    // Enforce a single GetDRIP style skill per project:
    // only count folders that have a skill.json with author: "getdrip".
    if (fs.existsSync(skillsDir)) {
      const existingDripSkills = fs
        .readdirSync(skillsDir)
        .map((entry) => {
          const fullPath = path.join(skillsDir, entry);
          return { name: entry, path: fullPath };
        })
        .filter(({ path: fullPath }) => {
          if (!fs.existsSync(fullPath) || !fs.statSync(fullPath).isDirectory()) {
            return false;
          }

          const manifestPath = path.join(fullPath, 'skill.json');
          if (!fs.existsSync(manifestPath)) {
            return false;
          }

          try {
            const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
            return manifest.author === 'getdrip';
          } catch {
            return false;
          }
        });

      const otherDripSkills = existingDripSkills.filter(
        ({ name, path: fullPath }) => name !== skillName && fullPath !== skillPath
      );

      if (otherDripSkills.length > 0) {
        console.log("Only one GetDRIP style skill is allowed per project. Please remove the existing style skill using the 'npx getdrip remove <style-skill-name>' command before adding a new one.");
        process.exit(1);
      }
    }
    
    // Download the repo zip
    const zipBuffer = await downloadRepoZip();
    
    // Extract the skill folder
    await extractSkillFolder(zipBuffer, skillName, skillsDir);

    // Write agent-frontend-instruction.md to project root
    const instructionPath = path.join(process.cwd(), 'agent-frontend-instruction.md');
    const instructionContent = `# Frontend Agent Instructions — GetDRIP

This project uses the **${skillName}** design skill from GetDRIP.

## Before creating or modifying any frontend UI, you MUST:

1. Read \`skills/${skillName}/SKILL.md\` — it contains the design laws and usage instructions.
2. Use only the design tokens defined in \`skills/${skillName}/tokens/\` (colors, typography, spacing, shadows, motion).
3. Follow the component patterns in \`skills/${skillName}/components/\` as reference.
4. Read \`skills/${skillName}/philosophy.md\` to understand the design intent and mood.

## Core rule:

Never introduce styles, colors, fonts, or visual patterns that contradict the skill's design system. Every UI element you create or edit must be consistent with the **${skillName}** design language.

> Generated by \`npx getdrip add ${skillName}\`
`;
    fs.writeFileSync(instructionPath, instructionContent);

    // Inject GetDRIP block into platform agent files (CLAUDE.md, .cursorrules, .windsurfrules)
    injectAgentInstructions(skillName, process.cwd());

    // Read skill.json to check dependencies
    const skillJsonPath = path.join(skillPath, 'skill.json');
    if (!fs.existsSync(skillJsonPath)) {
      console.warn(`Warning: skill.json not found at ${skillJsonPath}`);
      printSuccessMessage(skillName);
      return;
    }
    
    const skillJson = JSON.parse(fs.readFileSync(skillJsonPath, 'utf8'));
    const projectPackageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
    
    // Check for missing dependencies
    const dependencies = skillJson.dependencies || {};
    const missingDeps = [];
    
    const allDeps = {
      ...(projectPackageJson.dependencies || {}),
      ...(projectPackageJson.devDependencies || {})
    };
    
    for (const [depName, depVersion] of Object.entries(dependencies)) {
      if (!allDeps[depName]) {
        missingDeps.push(`${depName}@${depVersion}`);
      }
    }
    
    // Install missing dependencies
    if (missingDeps.length > 0) {
      console.log(`Installing missing dependencies: ${missingDeps.join(', ')}`);
      try {
        execSync(`npm install ${missingDeps.join(' ')}`, {
          stdio: 'inherit',
          cwd: process.cwd()
        });
      } catch (error) {
        console.warn('Warning: Failed to install some dependencies. You may need to install them manually.');
      }
    }
    
    // Print success message
    printSuccessMessage(skillName);
    
  } catch (error) {
    console.error(`Error: ${error.message}`);
    process.exit(1);
  }
}

module.exports = {
  addSkill
};
